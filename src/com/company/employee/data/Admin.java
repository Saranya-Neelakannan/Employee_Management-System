public class Admin {
	
	public int AdminId;
	public String adminName;
	public String password;
	public String role;
	
	

	public Admin(int adminId, String adminName, String password, String role) {
		
		this.AdminId = adminId;
		this.adminName = adminName;
		this.password = password;
		this.role = role;

	}

}
